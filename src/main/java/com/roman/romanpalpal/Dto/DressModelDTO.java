package com.roman.romanpalpal.Dto;

public class DressModelDTO {

    private String modelNo;
    private String modelName;
    private int modelPrice;
    private String modelColor;
    private String modelSize;
    private String modelSizeSummary;

    public String getModelNo() {
        return modelNo;
    }

    public void setModelNo(String modelNo) {
        this.modelNo = modelNo;
    }

    public String getModelName() {
        return modelName;
    }

    public void setModelName(String modelName) {
        this.modelName = modelName;
    }

    public int getModelPrice() {
        return modelPrice;
    }

    public void setModelPrice(int modelPrice) {
        this.modelPrice = modelPrice;
    }

    public String getModelColor() {
        return modelColor;
    }

    public void setModelColor(String modelColor) {
        this.modelColor = modelColor;
    }

    public String getModelSize() {
        return modelSize;
    }

    public void setModelSize(String modelSize) {
        this.modelSize = modelSize;
    }

    public String getModelSizeSummary() { return modelSizeSummary; }

    public void setModelSizeSummary(String modelSizeSummary) { this.modelSizeSummary = modelSizeSummary; }


}
